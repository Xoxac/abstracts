import math    # импорт всего модуля math
print(math.pi)  # 3.141592653589793

from math import *  # импортируем всё из модуля math это позволяет обращаться к функциям, не указывая имя модуля
print(pi)  # 3.141592653589793

import math as m  # использование нового имени для обращения к импортированному модулю
print(m.pi)  # 3.141592653589793

