# Иногда на практике используется форма оператора for с блоком else.
# если оператор цикла прекращает свое выполнение вследствие вызова инструкции break,
# то команды в else-блоке не выполняются

mytext=input("ins text: ")
symbs=['a', 'f', 'z']
print("searching: ", symbs)
