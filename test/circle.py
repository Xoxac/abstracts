# Программа, при выполнении которой пользователь
# вводит число, после чего выполняется проверка того, является ли это
# число простым (если оно не имеет никаких делителей, кроме единицы и самого себя)
# В программе реализован следующий алгоритм: сначала вводится чис-
# ло (которое проверяется), а затем последовательно вычисляется остаток
# от деления этого числа на 2, 3, 4 и так далее. Если остаток от деления
# равен нулю, это означает, что у числа есть соответствующий делитель.
# В таком случае число не является простым. Если окажется, что у числа
# делителей нет, значит, оно простое.

number=int(input("ins: "))
num=number//2 # верхняя граница делителя - самый большой делитель не может превышать половины от числа
k=2 # начальное значение делителя

while k<=num: # поиск делителей
    if number%k==0:
        print("число не простое")
        break
    else:
        k=k+1
else:
    print("число простое")
print("проверка завершена")

# Далее на примере похожей программы мы проиллюстрируем использо-
# вание инструкции continue. В программе, представленной выше,
# для введенного пользователем числа определяются все его делители.

number=int(input("ins: "))
print("делится на ",1)
k=1 # начальное значение делителя
while k<number//2:
    k=k+1
    if number%k!=0:
        continue # начать цикл сызнова (здесь можно было и без continue)
    print("делится на ", k)
print("делится на ", number)


